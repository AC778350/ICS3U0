print("There are ", 20, " people in this room.");
# using a format specifier:
print("There are %d people in this room." % 20)

print("The total money spent is: ", (1.75+7.25+13.32))
# using a format specifier:
print("The total money spent is: %.2f." % (1.75+7.25+13.32))

print("Pi is equal to %f." % 3.1416)

print("%s's final exam score is %d." % ("Bob", 89))

print("The area of a circle of radius %d is %.4f units squared." % (7, 3.1416*7*7))

